            <section class="intro">
                <h1>¿Qué es la violencia de género?</h1>
                <p>
                    Se entiende como violencia al uso de la fuerza para conseguir un
                    fin, especialmente para dominar a alguien o imponer algo, es un
                    comportamiento muy frecuente en todas las sociedades. Tiene, por
                    supuesto, una derivación legítima cuando es usada por el Estado de
                    derecho de acuerdo a la ley, pero el concepto de violencia que aquí
                    importa es el aplicado de forma coercitiva para la dominación de
                    alguien contra la ley y contra la voluntad y libertad del violentado.
                </p>
                <p>
                    La OMS la define como el uso deliberado de la fuerza física o el poder, ya sea en grado
                    de amenaza o efectivo, contra uno mismo, otra persona o un grupo o comunidad,
                    que cause o tenga muchas probabilidades de causar lesiones, muerte, daños
                    psicológicos, trastornos del desarrollo o privaciones. Es especialmente
                    importante el aspecto de la intencionalidad. Se trata de una conducta
                    deliberada, que se realiza de manera consciente y adrede para generar
                    algún tipo de daño a la víctima. La intencionalidad de las conductas
                    excluye de esta definición a los accidentes e incluye el uso de
                    la agresión para resolver desavenencias. En este sentido,
                    las teorías explicativas más relevantes son las biológicas, las
                    psicosociales, las socio dinámicas y las de carácter estructural. 
                </p>

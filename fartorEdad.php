                    <h3>Cambios en la Edad de las Víctimas y Tendencias Recientes</h3>
                    <p>
                        Según los propios datos del INE, las víctimas correspondientes al
                        grupo de edad de 18-19 años descendieron en 2017 un 26.5% con
                        respecto al año anterior; las víctimas de 20 a 24 años también cayeron
                        un 3,1% y las menores de 18 años, un 2,6%. En el año 2011, el pico de
                        violencia sobre la mujer se situaba sobre las víctimas de entre 30 y 34
                        años, que se ha desplazado a las mujeres de entre 35 y 39 años. Con
                        todo, es lógico que, estando la violencia de género por definición ligada a la relación de
                        pareja, las más de las veces esta ocurra dentro de ella o en fases posteriores al término
                        de la misma y eso, de una forma u otra, lo aproxima al ciclo reproductivo de la mujer.
                    </p>
                    <p>
                        La distribución de frecuencias por grupos de edad nos sitúa la franja entre los 18 y 44 años
                        como la época de mayor riesgo. Dentro de estos grupos, podemos apreciar como los grupos
                        de edad con mayor porcentaje de víctimas se sitúan entre los 25 y 34 años. Además, a pesar
                        del fuerte descenso en el año 2013 con respecto al 2011, en el año 2015 ya se hacía notar una
                        cierta elevación de víctimas de violencia de género. De hecho, se puede apreciar como los
                        niveles de 2017 casi han igualado a los datos de 2011, si bien con un desplazamiento de la
                        moda a grupos de mayor edad. No hay, pues, ninguna evidencia de que la población joven
                        (14-29) se vea hoy más afectada por la violencia de género de lo que estaba hace siete años.                         
                    </p>
